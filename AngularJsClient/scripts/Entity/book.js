function Book(bookID, name, author, bookType, isbn, bookPrice, offerPrice, remark) {
    this.BookID = bookID;
    this.Name = name;
    this.Author = author;
    this.BookType = bookType;
    this.ISBN = isbn;
    this.BookPrice = bookPrice;
    this.OfferPrice = offerPrice;
    this.Remark = remark;
}